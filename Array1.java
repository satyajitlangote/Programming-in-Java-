public class Array1
{
    public static void main(String args[])
    {
        int a[]={1,2,3,4,5};
        System.out.println("Value of the array");
        for(int i=0;i<5;i++)
        {
            System.out.println(a[i]);
        }
        System.out.println("Value of an array reverse order ");
        for(int i=41;i>=0;i--)
        {
            System.out.println(a[i]);
        }
    }
}
