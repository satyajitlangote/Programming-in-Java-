public class SumofArray
{
    public static void main(String args[])
    {
        int a[]={1,2,3,4,5};
        int sum=0;
        System.out.println("Value sof an array ");
        for(int i=0;i<5;i++)
        {
            sum=sum+a[i];
        }
        System.out.println("Sum of the values of an array:-" +sum);
    }    
}
